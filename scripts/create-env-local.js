#!/usr/bin/env node
/**
 * Erzeugt .env.local aus Subdomain + Region (einfachste Methode).
 * Usage:
 *   VITE_NHOST_SUBDOMAIN=xxx VITE_NHOST_REGION=eu-central-1 node scripts/create-env-local.js
 *   node scripts/create-env-local.js <subdomain> [region]
 *   npm run env:local -- mthatsgmfklegprnulnn eu-central-1
 */
const fs = require('fs');
const path = require('path');

const subdomain = process.env.VITE_NHOST_SUBDOMAIN || process.argv[2];
const region = process.env.VITE_NHOST_REGION || process.argv[3] || 'eu-central-1';

if (!subdomain || !subdomain.trim()) {
  console.error('Subdomain fehlt.');
  console.error('  VITE_NHOST_SUBDOMAIN=xxx node scripts/create-env-local.js');
  console.error('  node scripts/create-env-local.js <subdomain> [region]');
  process.exit(1);
}

const base = `https://${subdomain.trim()}`;
const r = region.trim();
const content = `# Generated by scripts/create-env-local.js - nur Subdomain + Region
VITE_NHOST_SUBDOMAIN=${subdomain.trim()}
VITE_NHOST_REGION=${r}
VITE_NHOST_AUTH_URL=${base}.auth.${r}.nhost.run/v1
VITE_NHOST_GRAPHQL_URL=${base}.graphql.${r}.nhost.run/v1
VITE_NHOST_STORAGE_URL=${base}.storage.${r}.nhost.run/v1
VITE_NHOST_FUNCTIONS_URL=${base}.functions.${r}.nhost.run/v1
VITE_APP_WEB_URL=http://localhost:3000
VITE_AUTH_REDIRECT_URL=http://localhost:3000
VITE_PASSWORD_RESET_REDIRECT_URL=http://localhost:3000/reset-password
`;

const outPath = path.join(__dirname, '..', '.env.local');
fs.writeFileSync(outPath, content, 'utf8');
console.log('Written', outPath);
console.log('Subdomain:', subdomain.trim(), '| Region:', r);
console.log('Run: npm run dev');
